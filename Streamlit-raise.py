def get_shared_context(titles1, titles2):
    words1 = set(re.findall(r'\w+', " ".join(titles1).lower()))
    words2 = set(re.findall(r'\w+', " ".join(titles2).lower()))
    stop = {'the', 'and', 'for', 'with', 'from', 'this', 'that', 'ai', 'impact', 'using', 'report', 'research'}
    common = [w for w in words1.intersection(words2) if len(w) > 4 and w not in stop]
    return ", ".join(common[:3])

