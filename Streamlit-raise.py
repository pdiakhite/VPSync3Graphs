map_counts = {}

for _, row in df.iterrows():
    title = str(row.get('title', '')).lower()
    matched_loc = next((l for l in COORDS if l.lower() in title), None)
    if matched_loc:
        classes = str(row.get('classes_str', '')).lower()
        group = next((g for g, info in CATEGORY_MAP.items() if any(kw in classes for kw in info['keywords'])), None)
        if group:
            if matched_loc not in map_counts:
                map_counts[matched_loc] = {'coords': COORDS[matched_loc], 'groups': {}, 'titles': []}
            map_counts[matched_loc]['groups'][group] = map_counts[matched_loc]['groups'].get(group, 0) + 1
            map_counts[matched_loc]['titles'].append(str(row['title']))

