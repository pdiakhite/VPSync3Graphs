def get_sentiment_strict(text):
    if pd.isna(text) or text.strip() == "": return "Neutral"

    prompt = f"""
    You are a highly sensitive Sentiment Analyst.
    Classify the HUMAN IMPACT of this news summary as Positive, Negative, or Neutral.

    EXAMPLES:
    - "AI improves cancer detection rates by 30%" -> Positive
    - "AI-generated deepfakes are used to bully students" -> Negative
    - "The company released its quarterly AI report on Tuesday" -> Neutral

    STRICT RULES:
    1. If there is ANY risk, harm, or fear, you MUST choose 'Negative'.
    2. If there is ANY benefit, progress, or joy, you MUST choose 'Positive'.
    3. Output ONLY the word: Positive, Negative, or Neutral.

    SUMMARY: {text}
    """
    try:
        res = ollama.chat(
            model='llama3.2',
            messages=[{'role': 'user', 'content': prompt}],
            options={'temperature': 0}
        )
        answer = res['message']['content'].strip().replace('.', '').capitalize()
        if "Positive" in answer: return "Positive"
        if "Negative" in answer: return "Negative"
        return "Neutral"
    except:
        return "Neutral"

